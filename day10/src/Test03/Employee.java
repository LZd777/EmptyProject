package Test03;

public class Employee {
    private String name;
    private MyDate myDate;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public MyDate getMyDate() {
        return myDate;
    }

    public void setMyDate(MyDate myDate) {
        this.myDate = myDate;
    }

    public double earning(){
        return earning();
    }

    public String getInfo(){
        return name + earning();
    }

}
